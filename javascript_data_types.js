
// Number: Representa tanto números enteros como de punto flotante.
let numeroEntero = 42;
let numeroDecimal = 3.14;

// String: Cadena de texto, compuesta por cero o más caracteres y van dentro de comillas
let cadenaTexto = "Hola, Mundo";

// Boolean: Representa un valor lógico que puede ser true o false.
let esVerdadero = true;
let esFalso = false;

// Object: Almacena colecciones de propiedades y métodos.
let objetoPersona = { 
    nombre: "Ana", 
    edad: 25 
};

// Array: Almacena elementos en un índice numérico.
let listaFrutas = ["manzana", "naranja", "plátano"];

// Function: Bloque de código que se puede ejecutar en cualquier momento.
function saludar() {
    console.log("Hola");
}

// Undefined: Variable que ha sido declarada pero no inicializada.
let variableIndefinida;
console.log(variableIndefinida); // undefined

// Null: Representa la ausencia intencional de cualquier objeto o valor.
let valorNulo = null;

// Symbol: Identificador único y inmutable utilizado para propiedades en objetos.
let idUnico = Symbol("id");

// BigInt: Representa números enteros muy grandes.
let numeroGrande = 1234567890123456789012345678901234567890n;
