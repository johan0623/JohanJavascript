// El metodo unshift en los arrays, recordar que el unshift es para agregar algo al principio del array


// unshift agrega al principio
const frutas = ["sandia", "pera"];

frutas.unshift("banana");
console.log(frutas);


// Esto agregara banana al principio del array llamado frutas, recordar que siempre se coloca el nombre del array y luego el metodo